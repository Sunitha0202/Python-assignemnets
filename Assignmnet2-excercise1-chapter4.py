# Initialize the starting number
num = 1

# Use a while loop to iterate through the numbers from 1 to 1000
while num <= 1000:
    # Check if the number is divisible by 3
    if num % 3 == 0:
        # If divisible by 3, print the number
        print(num)
    # Increment the number by 1 in each iteration
    num += 1
